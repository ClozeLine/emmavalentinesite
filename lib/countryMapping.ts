export function getDisplayName(normalizedName: string): string {
  return countryDisplayNames[normalizedName] ||
    normalizedName.split('_').map(w => w.charAt(0).toUpperCase() + w.slice(1)).join(' ');
}

export function getCountryId(normalizedName: string): string | undefined {
  return countryNameToId[normalizedName];
}

export function normalizeCountryName(name: string): string {
  return name.toLowerCase().replace(/\s+/g, '_').replace(/[^a-z_]/g, '');
}
