// Reverse mapping: ID to normalized name
export const countryIdToName: Record<string, string> = Object.entries(
  countryNameToId
).reduce(
  (acc, [name, id]) => {
    // Only keep the first (canonical) name for each ID
    if (!acc[id]) {
      acc[id] = name;
    }
    return acc;
  },
  {} as Record<string, string>
);

